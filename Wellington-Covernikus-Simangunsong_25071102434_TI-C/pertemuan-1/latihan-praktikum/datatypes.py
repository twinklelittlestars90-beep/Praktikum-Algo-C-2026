#Dalam python, tipe data sudah otomatis diatur ketika sebuah value di-assign ke variabel

a = "Hello world"                               #string
b = 314                                         #integer/bilangan bulat
c = 3.14                                        #float/bilangan desimal
d = 1j                                          #complex
e = ["Apel", "Pisang", "Ceri"]                  #list
f = ("Apel", "Pisang", "Ceri")                  #tuple
g = range(6)                                    #range
h = {"Nama" : "Wellin", "umur" : 19}            #dict
i = {"Apel", "Pisang", "Ceri"}                  #set
j = frozenset({"Apel", "Pisang", "Ceri"})       #frozenset
k = True                                        #boolean
l = b"Hello"                                    #bytes
m = bytearray(5)                                #bytearray
n = memoryview(bytes(5))                        #NoneType
o = None             

print(a)
print(b)
print(c)
print(d)
print(e)
print(f)
print(g)
print(h)
print(i)
print(j)
print(k)
print(l)
print(m)
print(n)
print(o)