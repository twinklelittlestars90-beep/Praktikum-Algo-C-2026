# terdapat 3 tipe data numerik di Python: integer, float, dan complex

a = 2
b = 3.5
c = 1j
print(type(a)) # output: <class 'int'>, karena a bertipe data integer
print(type(b)) # output: <class 'float'>, karena b bertipe data float
print(type(c)) # output: <class 'complex'>, karena c bertipe data complex