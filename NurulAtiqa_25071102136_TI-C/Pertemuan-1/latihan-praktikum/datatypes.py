# untuk mengetahui tipe data dari sebuah nilai, dapat menggunakan fungsi type()
print(type("atiqa")) # output: <class 'str'>, karena nilai "atiqa" bertipe data string
print(type(10)) # output: <class 'int'>, karena nilai 10 bertipe data integer
print(type(3.14)) # output: <class 'float'>, karena nilai 3.14 bertipe data float
print(type(True)) # output: <class 'bool'>, karena nilai True bertipe data boolean
print(type(False)) # output: <class 'bool'>, karena nilai False bertipe data boolean
