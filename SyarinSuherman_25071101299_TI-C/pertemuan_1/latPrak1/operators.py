# --- BAB: OPERATORS ---

a = 10
b = 3

# 1. Arithmetic
print(a + b)  # Tambah
print(a % b)  # Modulus (Sisa bagi) -> 1
print(a ** b) # Pangkat -> 1000
print(a // b) # Floor Division (Pembagian bulat) -> 3

# 2. Comparison (Perbandingan)
print(a == b) # Sama dengan
print(a != b) # Tidak sama dengan

# 3. Logical Operators
print(a > 5 and b < 10) # True jika keduanya benar
print(a > 5 or b > 10)  # True jika salah satu benar
print(not(a > 5))       # Membalikkan hasil (True jadi False)