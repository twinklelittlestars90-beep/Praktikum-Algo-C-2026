#Match
#sama sepert case pada bahasa c
#berfungsi mencari nilai yang sesuai dengan kondisi yang ada
day = 4
match day:
  case 6:
    print("Today is Saturday")
  case 7:
    print("Today is Sunday")
  case _:
    print("Looking forward to the Weekend")
#_ berfungsi sebagai jika tidak ada kondisi yang sesuai

month = 5
day = 4
match day:
  case 1 | 2 | 3 | 4 | 5 if month == 4:
    print("A weekday in April")
  case 1 | 2 | 3 | 4 | 5 if month == 5:
    print("A weekday in May")
  case _:
    print("No match")
