a = int(3) 
b = int(3.7) #angka dibelakang koma akan di hapus atau dibulatkan ke bawah
c = int("3") #string akan berubah menjadi integer   
print (a,type(a))
print (b,type(b))
print (c,type(c))

x = float(1) #akan menjadi 1.0   
y = float(7.8) #akan menjadi 7.8  
z = float("12") #akan menjadi 12.0
print (x,type(x))
print (y,type(y))
print (z,type(z))

x = str(1) #akan menjadi 1   
