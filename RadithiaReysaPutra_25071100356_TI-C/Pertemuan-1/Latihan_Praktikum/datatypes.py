#tipe data
tipe_string = "Halo"
print(type(tipe_string))

tipe_integer = 10
print(type(tipe_integer))

tipe_float = 3.14
print(type(tipe_float))

tipe_complex = 2 + 3j
print(type(tipe_complex))

tipe_list = [1, 2, 3]
print(type(tipe_list))

tipe_tuple = (1, 2, 3)
print(type(tipe_tuple))

tipe_range = range(1, 5)
print(type(tipe_range))

tipe_dictionary = {"a": 1}
print(type(tipe_dictionary))

tipe_set = {1, 2, 3}
print(type(tipe_set))

tipe_frozenset = frozenset([1, 2, 3])
print(type(tipe_frozenset))

tipe_boolean = True
print(type(tipe_boolean))

tipe_bytes = b"ABC"
print(type(tipe_bytes))

tipe_bytearray = bytearray(3)
print(type(tipe_bytearray))

tipe_memoryview = memoryview(tipe_bytes)
print(type(tipe_memoryview))

tipe_none = None
print(type(tipe_none))
