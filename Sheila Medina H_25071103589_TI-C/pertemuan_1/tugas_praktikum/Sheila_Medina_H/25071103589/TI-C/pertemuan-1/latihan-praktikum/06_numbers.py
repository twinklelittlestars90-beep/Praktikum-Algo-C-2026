
# Python memiliki tiga tipe data angka utama:
# int, float, dan complex

# 1. Integer (int)
a = 10
b = -5
print(a)
print(b)
print(type(a))

# 2. Float (float)
x = 3.14
y = 2.0
print(x)
print(y)
print(type(x))

# 3. Complex
z = 2 + 3j
print(z)
print(type(z))

# 4. Operasi aritmatika sederhana
jumlah = a + x
kurang = a - x
kali = a * x
bagi = a / x

print("Jumlah:", jumlah)
print("Kurang:", kurang)
print("Kali:", kali)
print("Bagi:", bagi)

# 5. Konversi tipe angka

angka_int = int(3.9)
angka_float = float(5)

print(angka_int)
print(angka_float)
print(type(angka_int))
print(type(angka_float))