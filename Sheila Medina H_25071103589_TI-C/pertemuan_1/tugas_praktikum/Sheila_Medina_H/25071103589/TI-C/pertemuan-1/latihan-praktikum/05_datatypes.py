# Data type adalah jenis data yang disimpan dalam variabel

# 1. Integer (int)
angka = 10
print(angka)
print(type(angka))

# 2. Float (float)
desimal = 3.5
print(desimal)
print(type(desimal))

# 3. String (str)
teks = "Belajar Python"
print(teks)
print(type(teks))

# 4. Boolean (bool)
status = True
print(status)
print(type(status))

# 5. List
buah = ["apel", "pisang", "jeruk"]
print(buah)
print(type(buah))

# 6. Tuple
angka_tuple = (1, 2, 3)
print(angka_tuple)
print(type(angka_tuple))

# 7. Dictionary
mahasiswa = {
    "nama": "Shel",
    "umur": 18,
    "aktif": True
}
print(mahasiswa)
print(type(mahasiswa))

# 8. Set (tidak berurutan, tidak duplikat)
angka_set = {1, 2, 3, 3}
print(angka_set)
print(type(angka_set))

# Catatan: Python adalah bahasa pemrograman yang dinamis, sehingga tipe data variabel dapat

