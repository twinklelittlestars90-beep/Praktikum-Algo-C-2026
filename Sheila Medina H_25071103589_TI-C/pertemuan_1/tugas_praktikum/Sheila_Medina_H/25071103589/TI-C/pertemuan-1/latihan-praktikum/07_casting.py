# Casting digunakan untuk mengubah tipe data

# 1. Casting ke integer (int)
angka_float = 3.7
angka_int = int(angka_float)

print(angka_float)
print(angka_int)
print(type(angka_int))

# 2. Casting ke float (float)
angka_bulat = 10
angka_desimal = float(angka_bulat)

print(angka_bulat)
print(angka_desimal)
print(type(angka_desimal))

# 3. Casting ke string (str)
umur = 18
umur_str = str(umur)

print(umur_str)
print(type(umur_str))

# 4. Casting dari string ke integer
angka_teks = "25"
angka_hasil = int(angka_teks)

print(angka_hasil)
print(type(angka_hasil))