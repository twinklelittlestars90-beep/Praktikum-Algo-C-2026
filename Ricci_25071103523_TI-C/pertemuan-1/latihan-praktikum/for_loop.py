#Dengan perulangan for, kita dapat mengeksekusi serangkaian pernyataan, 
# satu kali untuk setiap item dalam daftar, tuple, set, dan lain sebagainya.
#contoh 
fruits = ["apple", "banana", "cherry"]
for x in fruits:
  print(x)


#Fungsi range()mengembalikan serangkaian angka, dimulai dari 0 secara default, dan bertambah 1 (secara default), 
# dan berakhir pada angka yang ditentukan.
for x in range(6):
  print(x)
