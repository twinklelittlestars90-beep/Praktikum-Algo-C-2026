# BOOLEAN

print(10 > 9)   # True
print(10 == 9)  # False
print(10 < 9)   # False
# Boolean hanya memiliki dua nilai yaitu True (1) dan False (0) yang
# digunakan untuk operasi perbandingan dimana True dihasilkan jika operasi
# tersebut bernilai benar sedangkan False dihasilkan jika operasi tersebut
# bernilai salah.
