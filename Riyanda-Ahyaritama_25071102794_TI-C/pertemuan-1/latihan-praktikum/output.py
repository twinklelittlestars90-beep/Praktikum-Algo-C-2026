# PRINT TEKS
# PRINT SEDERHANA

print("Hello World!")
# Ini adalah pernyataan untuk menampilkan teks `Hello World!` dan diakhiri
# dengan \n (newline).
# ============================================================================ #


# DOUBLE QUOTE & SINGLE QUOTE

print("Hello World!") # Pernyataan ini benar secara sintaksis.
print('Hello World!') # Pernyataan ini juga benar.
# Menggunakan Double Quote (`"`) atau Single Quote (`'`) adalah preferensi
# masing-masing, keduanya benar secara aturan penulisan.
# ============================================================================ #


# PRINT YANG TIDAK DIAKHIRI NEWLINE

print("Hello", end=" ") # Ini akan diakhiri dengan Spasi (` `).
print("World!") # Sedangkan ini tetap diakhiri dengan Newline (\n).
# ============================================================================ #


