# SET TIPE DATA OTOMATIS
x = "Hello World!"                              # string
x = 20                                          # integer
x = 20.5                                        # float
x = 1j                                          # complex
x = ["apple", "banana", "cherry"]               # list
x = ("apple", "banana", "cherry")               # tuple
x = range(6)                                    # range
x = {"name": "Riyanda", "age": 19}              # dict
x = {"apple", "banana", "cherry"}               # set
x = frozenset({"apple", "banana", "cherry"})    # frozenset (set yang tidak dapat diubah)
x = True                                        # boolean
x = b"Hello"                                    # bytes
x = bytearray(5)                                # array byte
x = memoryview(bytes(5))                        # memory
x = None                                        # NoneType
# ============================================================================ #


# SET TIPE DATA OTOMATIS SPESIFIK
x = str("Hello World!")                         # string
x = int(20)                                     # integer
x = float(20.5)                                 # float
x = complex(1j)                                 # complex
x = list(["apple", "banana", "cherry"])         # list
x = tuple(("apple", "banana", "cherry"))        # tuple
x = range(6)                                    # range
x = dict({"name": "Riyanda", "age": 19})        # dict
x = set({"apple", "banana", "cherry"})          # set
x = frozenset({"apple", "banana", "cherry"})    # frozenset (set yang tidak dapat diubah)
x = bool(5)                                     # boolean
x = bytes(5)                                    # bytes
x = bytearray(5)                                # array byte
x = memoryview(bytes(5))                        # memory
# ============================================================================ #