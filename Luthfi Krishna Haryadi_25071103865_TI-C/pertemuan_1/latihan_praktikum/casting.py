#Casting

#Integer
x = int(3)   # x jadi 3
y = int(2.2) # y jadi 2
z = int("9") # z will be 9
#mengubah semua nilai menjadi integer, baik dari integer, float, ataupun dari string

#Float
x = float(2)     # x will be 2.0
y = float(4.8)   # y will be 4.8
z = float("5")   # z will be 5.0
w = float("11.9") # w will be 11.9
#mengubah semua nilai menjadi float, baik dari integer, float, ataupun dari string

#String
x = str("6e") # x will be '6e'
y = str(5)    # y will be '2'
z = str(5.6)  # z will be '5.6'
#mengubah semua nilai menjadi string, baik dari integer,float, ataupun dari string
