# menentukan tipe data dengan casting

# 1. integer
x = int(10)        # dari bilangan int ke int
y = int(3.8)       # dari float ke int
z = int("25")      # dari string angka ke int

# 2. float
a = float(5)       # dari int ke float
b = float("3.14")  # dari string angka desimal ke float

# 3. string
c = str(100)       # dari int ke string
d = str(7.5)       # dari float ke string

# menampilkan nilai dan tipe datanya
print(x, type(x))
print(y, type(y))
print(z, type(z))

print(a, type(a))
print(b, type(b))

print(c, type(c))
print(d, type(d))
